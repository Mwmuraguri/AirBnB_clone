import os.path
import json


class FileStorage:
    """ Rep an abstracted storage engine.
    Attributes:
        __file_path (str): Name of the file to save objects to
        __objects (dict): Dictionary of instantiated objects
    """

    __file_path = "file.json"
    __objects = {}

    def all(self):
        """returns the dict__objects"""
        return FileStorage.__objects

    def new(self, obj):
        """Set in __objects obj with key <obj_class_name>.id"""
        ocname = obj.__class__.__name__
        FileStorage.__objects["{}.{}".format(ocname, obj.id)] = obj

    def save(self):
        """Serialization of  __objects to the JSON-file filepath"""
        odict = FileStorage.__objects
        objdict = {obj: odict[obj].to_dict() for obj in odict.keys()}
        with open(FileStorage.__file_path, "w") as f:
            json.dump(objdict, f)

    def reload(self):
        """Deserialization of the JSON file _file path to objects, if it exist"""
        try:
            with open(FileStorage.__file_path) as f:
                objdict = json.load(f)
                for o in objdict.values():
                    cls_name = o["__class__"]
                    del o["__class__"]
                    self.new(eval(cls_name)(**o))
        except FileNotFoundError:
            return
